package upperpart.BasicKnowledg.startdemo;

public class VariableDemo3{
    public static void main(String[] args){
     //byte
     //byte的取值范围为"-128~127"
     byte b=10;

     //long
     //注意:如果要定义long类型的变量,那么在
     //数值的后面要加一个"L"作为后缀l可以是大写的也可以是小写的
     //建议写大写
     long n=999999L;
     System.out.println(n);

     //float
     //注意:定义float变量的时候
     //数据值后面也要加一个"F"为后缀9(大小写都可以)
     float m=10.1F;

     //boolean(布尔变量)
     //true false
     boolean o=true;
     System.out.println(o);

     //字符串用String(注意s要大写)
     String name="我是歌姬的爸爸";
     System.out.println(name);
    }
}