package upperpart.commonalgorithms.Lamda;

import java.util.Arrays;
import java.util.Comparator;

public class LamdaDemo2 {
    // 修复1：主方法必须加 public 修饰符，否则程序无法运行
    public static void main(String[] args) {

        // ======================================
        // 核心：可推导，可省略！编译器能自己推断的内容，都可以省
        // ======================================
        /*
         * 规则1：参数类型可以省略不写
         * 理解：函数式接口里的抽象方法已经定义了参数类型，编译器能根据接口自动推断，所以不用写
         * 例：Comparator的compare方法，参数本来就是Integer类型，Lambda里不用再写Integer
         *
         * 规则2：如果只有一个参数，参数类型可以省略，同时()也可以省略
         * 理解：多参数必须写()，但单参数时，连()都可以省
         * 注意：无参数时，()绝对不能省！比如() -> System.out.println("无参数")
         *
         * 规则3：如果Lambda方法体只有一行，大括号{}、分号;、return关键字可以同时省略,且只能同时省略
         * 理解：
         *  - 方法体只有一行，不用写大括号
         *  - 一行代码结尾的分号，也不用写
         *  - 如果这一行是return语句，return关键字也可以省略，编译器会自动把结果当成返回值
         * 注意：这三个要省就一起省，不能只省其中一个！
         */

        // ==================== @FunctionalInterface 注解详解 ====================
        /*
         * 1. 全称：函数式接口注解
         * 2. 书写位置：【只能写在 接口定义 的上方】，不能写在方法里、lambda前面！
         * 3. 作用：
         *    ✅ 强制检查该接口 有且仅有1个抽象方法（这是Lambda能使用的唯一前提）
         *    ✅ 如果你给接口多加了抽象方法，编译器直接报错
         * 4. 关键：
         *    我们这里用的 Comparator 接口，是JDK官方写好的，**它的源码已经加了@FunctionalInterface**
         *    所以我们自己的代码里，完全不需要再写这个注解！
         * 5. 只有【自己定义接口】时，才需要手动加这个注解
         */

        // 定义一个待排序的Integer数组
        Integer[] arr = {2, 3, 1, 5, 6, 7, 8, 4, 9};


        // ======================================
        // 写法1：传统匿名内部类写法（没用到Lambda）
        // ======================================
        Arrays.sort(arr, new Comparator<Integer>() {
            @Override
            public int compare(Integer o1, Integer o2) {
                return o1 - o2; // 核心逻辑：前一个减后一个，实现升序排序
            }
        });
        /*
         * 为什么这个写法啰嗦？
         *  1. 要写 new Comparator<Integer>() 接口名
         *  2. 要写 @Override 注解
         *  3. 要写方法名compare
         *  真正有用的只有 return o1 - o2; 这一行，其他都是模板代码
         */


        // 重置数组（不然上面的排序会影响下面的例子）
        arr = new Integer[]{2, 3, 1, 5, 6, 7, 8, 4, 9};


        // ======================================
        // 写法2：Lambda表达式「完整格式」
        // ======================================
        Arrays.sort(arr, (Integer o1, Integer o2) -> {
            return o1 - o2;
        });
        /*
         * 对比匿名内部类，Lambda省略了什么？
         *  1. 省略了 new Comparator<Integer>() 接口名
         *  2. 省略了 @Override 注解
         *  3. 省略了方法名compare（因为函数式接口只有1个抽象方法，编译器知道要实现哪个方法）
         *  4. 用 -> 代替了匿名内部类的大括号分隔
         */


        // 重置数组
        arr = new Integer[]{2, 3, 1, 5, 6, 7, 8, 4, 9};


        // ======================================
        // 写法3：Lambda表达式「省略写法」（最终版）
        // ======================================
        //o1-o2升序    o2-o1降序
        Arrays.sort(arr, (o1, o2) -> o2 - o1);
        /*
         * 这里用到了所有省略规则，我们一步步拆解：
         *  1. 省略了参数类型Integer（对应规则1，编译器能推断出o1/o2是Integer）
         *  2. 省略了大括号{}、分号;、return关键字（对应规则3，方法体只有一行return语句，三个同时省）
         *  3. 这里有两个参数，所以()不能省（规则2只适用于单参数场景）
         *
         * 最终代码只保留了核心逻辑，一眼就能看懂是在做什么
         */


        // 打印排序后的数组
        System.out.println(Arrays.toString(arr));
    }
}